// Glass2 Grove secondary OLED (SSD1309, 128x64) — Marauder Cardputer ADV build.
//
// M5GFX is not in Marauder's dependency tree (TFT_eSPI is used for the main
// display), so this drives the SSD1309 directly over Wire1.  The keyboard
// driver already calls Wire.begin(8, 9) for the TCA8418 — Wire1 on G2/G1
// keeps the two buses completely independent.
//
// SSD1309 is command-compatible with SSD1306; the init sequence is identical.
// Include from exactly one translation unit (esp32_marauder.ino).

#include <Wire.h>

#define G2_ADDR  0x3C
#define G2_SDA   2
#define G2_SCL   1
#define G2_W     128
#define G2_PAGES 8     // 8 pages × 8 rows = 64px height

static TwoWire  _g2w(1);   // Wire1 — independent from keyboard's Wire
static bool     _g2_ready = false;

// ── SSD1309 low-level helpers ───────────────────────────────────────────────

static void _g2c(uint8_t cmd) {
    _g2w.beginTransmission(G2_ADDR);
    _g2w.write(0x00);   // control byte: Co=0, D/C#=0 → command
    _g2w.write(cmd);
    _g2w.endTransmission();
}

static void _g2pos(uint8_t page, uint8_t col) {
    _g2c(0xB0 | (page & 0x07));         // set page address
    _g2c(0x00 | (col & 0x0F));          // column low nibble
    _g2c(0x10 | ((col >> 4) & 0x0F));   // column high nibble
}

