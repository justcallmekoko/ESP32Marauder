// ── Public API ───────────────────────────────────────────────────────────────

inline void glass2Clear() {
    if (!_g2_ready) return;
    static const uint8_t blank[16] = {};
    for (uint8_t page = 0; page < G2_PAGES; page++) {
        _g2pos(page, 0);
        for (uint8_t x = 0; x < G2_W; x += 16) {
            _g2w.beginTransmission(G2_ADDR);
            _g2w.write(0x40);   // control byte: data stream
            uint8_t n = min((uint8_t)16, (uint8_t)(G2_W - x));
            _g2w.write(blank, n);
            _g2w.endTransmission();
        }
    }
}

static void _g2putchar(uint8_t page, uint8_t col, char c) {
    if (c < 0x20 || c > 0x7E) c = 0x20;
    const uint8_t* glyph = _g2font[c - 0x20];
    _g2pos(page, col);
    _g2w.beginTransmission(G2_ADDR);
    _g2w.write(0x40);
    for (uint8_t i = 0; i < 5; i++) _g2w.write(pgm_read_byte(&glyph[i]));
    _g2w.write(0x00);   // 1px spacing
    _g2w.endTransmission();
}

// Show up to four lines (truncated to 20 chars each). Null lines are blank.
inline void glass2Show(const char* l1, const char* l2 = nullptr,
                       const char* l3 = nullptr, const char* l4 = nullptr) {
    if (!_g2_ready) return;
    glass2Clear();
    const char* lines[4] = { l1, l2, l3, l4 };
    const uint8_t pages[4] = { 0, 2, 4, 6 };
    for (uint8_t ln = 0; ln < 4; ln++) {
        if (!lines[ln]) continue;
        uint8_t col = 0;
        for (const char* p = lines[ln]; *p && col < G2_W - 6; p++, col += 6) {
            _g2putchar(pages[ln], col, *p);
        }
    }
}

inline void glass2Init() {
    _g2w.begin(G2_SDA, G2_SCL, 400000UL);
    // Probe — device present?
    _g2w.beginTransmission(G2_ADDR);
    if (_g2w.endTransmission() != 0) { _g2_ready = false; return; }
    // SSD1309 init (command-compatible with SSD1306)
    const uint8_t cmds[] = {
        0xAE,       // display off
        0xD5, 0x80, // clock / osc freq
        0xA8, 0x3F, // mux ratio 63 (64 rows)
        0xD3, 0x00, // display offset 0
        0x40,       // start line 0
        0x8D, 0x14, // charge pump on
        0x20, 0x00, // horizontal addressing mode
        0xA1,       // segment remap (col 127 → SEG0)
        0xC8,       // COM scan direction (remapped)
        0xDA, 0x12, // COM pins alt config
        0x81, 0xCF, // contrast
        0xD9, 0xF1, // pre-charge period
        0xDB, 0x40, // VCOMH deselect level
        0xA4,       // output follows RAM
        0xA6,       // normal (non-inverted)
        0xAF,       // display on
    };
    for (uint8_t c : cmds) _g2c(c);
    _g2_ready = true;
    glass2Clear();
}
